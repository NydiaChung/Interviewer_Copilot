"""对话轮次辅助判定 — 问题信号词检测。"""

# ── 问题信号词汇 ─────────────────────────────────────────────────────────────
QUESTION_HINTS = (
    "?",
    "？",
    "吗",
    "么",
    "呢",
    "why",
    "how",
    "what",
    "which",
    "when",
    "where",
    "介绍",
    "说说",
    "讲讲",
    "谈谈",
    "聊聊",
    "解释",
    "原因",
    "怎么",
    "如何",
    "为什么",
    "有哪些",
    "能不能",
    "可不可以",
    "请你",
    "什么",
    "哪些",
    "哪种",
    "具体",
    "经验",
    "背景",
    "过程",
    "遇到",
    "说明",
    "描述",
    "是否",
)


def is_question_like(text: str) -> bool:
    """判断文本是否含问题信号词。"""
    t = (text or "").strip()
    if not t:
        return False
    return any(h in t for h in QUESTION_HINTS)
